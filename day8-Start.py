def greet():
  print("Hi")
  print("Hello")
  print("Bonjour")
greet()

def greet_with_name(name):
  print(f"Hello {name}")

greet_with_name("Aryan")

def greet_with(name, Location):
  print(f"Hello {name}")
  print(f"What is it like in {Location}")
greet_with("Aryan", "Agra")
